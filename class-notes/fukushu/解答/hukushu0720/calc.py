# ①	add関数	引数を２つ受け取り加算の結果を戻り値として返す
def add(a, b):
    ans = a + b
    return ans

# ②	sub関数	引数を２つ受け取り減算の結果を戻り値として返す
def sub(a, b):
    ans = a - b
    return ans

# ③	mul関数	引数を２つ受け取り乗算の結果を戻り値として返す
def mul(a, b):
    ans = a * b
    return ans

# ④	div関数	引数を２つ受け取り除算の結果を戻り値として返す
def div(a, b):
    ans = a / b
    return ans

